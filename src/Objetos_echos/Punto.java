public class Punto {
    private int x;
    private int y;
    
    //constructor
    public Punto(int x, int y) {
        this.x = x;
        this.y = y;
    }

    //get
    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
    
    //set
    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    
    
}
